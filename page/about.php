<h3>SEJARANG PT.JAYA MOTOR</h3>
	Perpustakaan Nasional merupakan Lembaga Pemerintah Nonkementrian yang berada di bawah dan bertanggung jawab kepada Presiden.Perpustakaan Nasional dipimpin oleh seorang Kepala.


<h3>TUGAS DAN FUNGSI</h3>
Perpustakaan Nasional melaksanakan tugas pemerintahan di bidang perpustakaan sesuai dengan ketentuan peraturan perundang-undangan meliputi:
	<ul type='disc' style="padding-left : 50px">
		<li>menetapkan kebijakan nasional,kebijakan umum,dan kebijakan teknis pengelolaan perpustakaan;</li>
		<li>melaksanakan pembinaan,pengembangan,evaluasi,dan koordinasi terhadap pengelolaan perpustakaan;</li>
		<li>membina kerja sama dalam pengelolaan berbagai jenis perpustakaan; dan</li>
		<li>mengembangkan standar nasional perpustakaan.</li>
	</ul>
