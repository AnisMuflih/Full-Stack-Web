<h3>SEJARAH PT.JAYA MOTOR</h3>
	PT.JAYA MOTOR awalnya terbentuk pada 21 april 1970 di pangkalpinang,yang asal mulanya adalah tempat reparasi khusus sepeda motor sekaligus tempat berkumpulnya klub-klub motor dan bertahan sampai sekarang.


<h3>TUGAS DAN FUNGSI</h3>
PT.JAYA MOTOR selaku perusahaan dibidang otomotif akan menjalankan tugas sebagai berikut:
	<ul type='disc' style="padding-left : 50px">
		<li>melaksanakan pembinaan,pengembangan,evaluasi,dan koordinasi terhadap pengelolaa perusahaan otomotif;</li>
		<li>membina kerja sama dalam pengelolaan berbagai jenis perusahaan di bidang otomotif; dan</li>
		<li>mengembangkan standar dunia otomotif.</li>
	</ul>
<h3>KELEBIHAN DAN PENGALAMAN PERUSAHAAN</h3>
	Sudah lebih dari 50 tahun PT.JAYA MOTOR memiliki pengalaman menekuni bisnis di bidang otomotif dan salah satu perusahaan tertua yang ada di kota pangkalpinang dan memiliki seluruh tenaga kerja yang kompeten di bidang nya demi kelancaran perusahaan maupun dalam kerja sama antar perusahaan dan industri.